package Bitwise_Operators;

public class Increment_Decrement_Operator {

	public static void main(String[] args) {
		
		int a = 10;
		a++;
		System.out.println(a);
		++a;
		System.out.println(a);
		
		System.out.println();
		int b = a++;
		System.out.println(b);
		System.out.println(a);
		
		System.out.println();
		int c = ++a;
		System.out.println(c);
		System.out.println(a);
		
		

	}

}
